export function getQuestionId(categoryIndex, questionIndex) {
  return `${categoryIndex}-${questionIndex}`;
}

export function getSlidePath(slideNumber) {
  return `/assets/slides/slide-${String(slideNumber).padStart(3, "0")}.png`;
}

export function getStageLabel(step, answerIndex, explanationCount) {
  if (step < answerIndex) {
    return "Вопрос";
  }

  if (step === answerIndex) {
    return explanationCount > 0 ? "Ответ открыт" : "Ответ";
  }

  return `Пояснение ${step - answerIndex} из ${explanationCount}`;
}
